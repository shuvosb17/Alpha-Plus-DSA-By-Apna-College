#include <iostream>
using namespace std;

int main() {
    // Single-line comment: declaring variable
    int age = 21;  

    /* Multi-line comment:
       We calculate the age for next year
       and print it on the screen.
    */
    int nextYearAge = age + 1;

    cout << "Current age: " << age << endl;      // display current age
    cout << "Next year age: " << nextYearAge;    // display next year's age

    return 0; // end of program
}
